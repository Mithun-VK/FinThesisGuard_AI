# 5. QueryResponse
# ─────────────────────────────────────────────

class QueryResponse(BaseModel):
    """
    Complete response for a financial RAG query.
    Returned by POST /api/query.
    Contains answer, citations, conflicts, confidence, and metadata.
    """

    answer: str = Field(
        ...,
        description="The synthesized answer with inline citation markers [1], [2] etc.",
    )
    citations: list[Citation] = Field(
        default_factory=list,
        description="List of source documents used in the answer",
    )
    confidence: ConfidenceLevel = Field(
        ...,
        description="Overall answer confidence based on source agreement and coverage",
    )
    reasoning: str = Field(
        default="",
        description="Agent's step-by-step reasoning chain (shown in collapsible UI panel)",
    )
    conflicts: list[Conflict] = Field(
        default_factory=list,
        description="List of detected contradictions across sources",
    )
    data_gaps: list[str] = Field(
        default_factory=list,
        description="Information the system could not find in the corpus",
    )
    latency_ms: int = Field(
        default=0,
        ge=0,
        description="Total pipeline execution time in milliseconds",
    )
    agents_used: list[str] = Field(
        default_factory=list,
        description="Names of agents that processed this query",
    )
    query_type: str = Field(
        default="UNKNOWN",
        description="Classified query type: FACTUAL, COMPARATIVE, REGULATORY, THESIS",
    )
    cache_hit: bool = Field(
        default=False,
        description="True if response was served from Redis cache",
    )
    sub_queries: list[str] = Field(
        default_factory=list,
        description="Decomposed sub-queries generated by Agent 2 (if applicable)",
    )
    agent_traces: list[AgentTrace] = Field(
        default_factory=list,
        description="Per-agent execution traces (populated in debug mode only)",
    )
    request_id: Optional[str] = Field(
        default=None,
        description="Unique request ID for tracing and support",
    )
    timestamp: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat(),
        description="UTC timestamp when response was generated",
    )

    # ── Computed Fields ───────────────────────

    @computed_field
    @property
    def citation_count(self) -> int:
        return len(self.citations)

    @computed_field
    @property
    def conflict_count(self) -> int:
        return len(self.conflicts)

    @computed_field
    @property
    def has_conflicts(self) -> bool:
        return len(self.conflicts) > 0

    @computed_field
    @property
    def within_latency_target(self) -> bool:
        """True if response was delivered within the 3-second target."""
        return self.latency_ms <= 3000

    @computed_field
    @property
    def regulatory_sources_count(self) -> int:
        """Count of citations from RBI/SEBI/MCA sources."""
        return sum(1 for c in self.citations if c.is_regulatory)

    # ── Convenience Constructors ──────────────

    @classmethod
    def error_response(
        cls,
        error_message: str,
        request_id: Optional[str] = None,
        latency_ms: int = 0,
    ) -> "QueryResponse":
        """Creates a standardized error response."""
        return cls(
            answer=f"Unable to process query: {error_message}",
            confidence="Low",
            reasoning=f"Pipeline error: {error_message}",
            data_gaps=["Query could not be processed due to an internal error."],
            latency_ms=latency_ms,
            request_id=request_id,
        )

    @classmethod
    def insufficient_data_response(
        cls,
        query: str,
        request_id: Optional[str] = None,
        latency_ms: int = 0,
    ) -> "QueryResponse":
        """Creates a response when corpus has no relevant data."""
        return cls(
            answer=(
                "Insufficient data available in the current corpus to answer this query. "
                "The relevant documents may not have been ingested yet. "
                "Please try ingesting the relevant annual reports, RBI circulars, "
                "or research notes before querying."
            ),
            confidence="Low",
            reasoning="No relevant chunks retrieved from Pinecone above similarity threshold.",
            data_gaps=[f"No sources found for: {query[:100]}"],
            latency_ms=latency_ms,
            request_id=request_id,
        )

    model_config = {"populate_by_name": True}


# ─────────────────────────────────────────────
# 6. BatchQueryResponse
# ─────────────────────────────────────────────

class BatchQueryResponse(BaseModel):
    """Response for batch query endpoint."""

    results: list[QueryResponse]
    total_queries: int
    successful: int
    failed: int
    total_latency_ms: int

    @computed_field
    @property
    def avg_latency_ms(self) -> float:
        if not self.results:
            return 0.0
        return round(
            sum(r.latency_ms for r in self.results) / len(self.results), 1
        )


