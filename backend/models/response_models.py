# 8. HealthResponse
# ─────────────────────────────────────────────

class HealthResponse(BaseModel):
    """
    Response for GET /health endpoint.
    Shows overall system status and per-service health.
    """

    status: ServiceStatus = Field(
        ...,
        description="Overall system status",
    )
    app_name: str = Field(
        default="FinThesisGuard AI",
        description="Application name",
    )
    version: str = Field(
        ...,
        description="Application version",
        examples=["1.0.0"],
    )
    environment: str = Field(
        ...,
        description="Deployment environment: development, staging, production",
    )
    uptime_seconds: float = Field(
        default=0.0,
        description="Application uptime in seconds",
    )
    services: dict[str, ServiceHealth] = Field(
        default_factory=dict,
        description="Health status of each dependent service",
    )
    corpus_stats: Optional[dict] = Field(
        default=None,
        description="Pinecone corpus statistics: total vectors, namespaces",
    )
    cache_stats: Optional[dict] = Field(
        default=None,
        description="Redis cache statistics: hit rate, total ops",
    )
    llm_stats: Optional[dict] = Field(
        default=None,
        description="LLM usage stats: total calls, success rate, tokens used",
    )
    timestamp: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat(),
    )

    @computed_field
    @property
    def uptime_human(self) -> str:
        """Human-readable uptime string."""
        seconds = int(self.uptime_seconds)
        days, rem = divmod(seconds, 86400)
        hours, rem = divmod(rem, 3600)
        minutes, secs = divmod(rem, 60)
        if days > 0:
            return f"{days}d {hours}h {minutes}m"
        if hours > 0:
            return f"{hours}h {minutes}m {secs}s"
        if minutes > 0:
            return f"{minutes}m {secs}s"
        return f"{secs}s"

    @computed_field
    @property
    def all_services_healthy(self) -> bool:
        return all(s.is_healthy for s in self.services.values())


