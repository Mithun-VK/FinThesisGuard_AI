# ─────────────────────────────────────────────
# 1. Citation
# ─────────────────────────────────────────────

class Citation(BaseModel):
    """
    Represents a single retrieved source document used in an answer.
    Attached to every QueryResponse and ThesisResponse.
    """

    id: int = Field(
        ...,
        ge=1,
        description="Citation number used inline in answer as [1], [2] etc.",
    )
    title: str = Field(
        ...,
        max_length=500,
        description="Document title or heading",
        examples=["HDFC Bank Q3 FY26 Earnings Call Transcript"],
    )
    source_type: str = Field(
        ...,
        description="Authority type: rbi, sebi, annual_report, earnings_transcript, etc.",
        examples=["annual_report", "sebi", "rbi", "broker_research"],
    )
    date: str = Field(
        default="unknown",
        description="Publication date in YYYY-MM-DD or YYYY-MM or YYYY format",
        examples=["2026-01-15", "2025-10", "2025"],
    )
    authority_weight: float = Field(
        default=1.0,
        ge=0.0,
        le=2.0,
        description="Source authority weight used in re-ranking (0.0–2.0)",
    )
    relevance_score: float = Field(
        default=0.0,
        ge=0.0,
        le=1.0,
        description="Final re-ranked relevance score (0.0–1.0)",
    )
    url: Optional[str] = Field(
        default=None,
        description="Source URL if publicly available",
        examples=["https://www.rbi.org.in/Scripts/NotificationUser.aspx?Id=12345"],
    )
    excerpt: str = Field(
        default="",
        max_length=1000,
        description="Relevant excerpt from the source document",
    )
    company: Optional[str] = Field(
        default=None,
        description="Company name if document is company-specific",
        examples=["HDFC Bank", "TCS"],
    )
    sector: Optional[str] = Field(
        default=None,
        description="Industry sector of the source document",
        examples=["banking", "technology", "pharma"],
    )

    @field_validator("source_type")
    @classmethod
    def normalize_source_type(cls, v: str) -> str:
        return v.lower().strip()

    @field_validator("relevance_score", "authority_weight")
    @classmethod
    def round_scores(cls, v: float) -> float:
        return round(v, 4)

    @computed_field
    @property
    def authority_label(self) -> str:
        """Human-readable label for the source authority."""
        labels = {
            "rbi":                  "RBI",
            "sebi":                 "SEBI",
            "mca":                  "MCA",
            "annual_report":        "Annual Report",
            "earnings_transcript":  "Earnings Transcript",
            "broker_research":      "Broker Research",
            "tax_law":              "Tax Law",
            "news":                 "News",
            "blog":                 "Blog",
            "unknown":              "Unknown",
        }
        return labels.get(self.source_type, self.source_type.upper())

    @computed_field
    @property
    def is_regulatory(self) -> bool:
        """True if source is a regulatory authority (RBI/SEBI/MCA)."""
        return self.source_type in {"rbi", "sebi", "mca", "tax_law"}


# ─────────────────────────────────────────────
# 2. Conflict
# ─────────────────────────────────────────────

class Conflict(BaseModel):
    """
    Represents a detected contradiction between two sources
    on the same metric/field. Generated by Agent 4.
    """

    field: str = Field(
        ...,
        description="The financial metric where conflict was detected",
        examples=["NPA ratio", "NIM", "Revenue", "CASA ratio"],
    )
    source_a: str = Field(
        ...,
        description="Name/title of the first source",
        examples=["HDFC Bank Q3 FY26 Annual Report"],
    )
    value_a: str = Field(
        ...,
        description="Value reported by source A",
        examples=["1.26%", "₹45,000 crore", "4.2%"],
    )
    source_b: str = Field(
        ...,
        description="Name/title of the second (conflicting) source",
        examples=["Motilal Oswal HDFC Research Note"],
    )
    value_b: str = Field(
        ...,
        description="Value reported by source B (conflicts with value_a)",
        examples=["1.34%", "₹43,200 crore", "4.5%"],
    )
    recommended_source: str = Field(
        ...,
        description="The source Agent 4 recommends as more reliable",
    )
    reason: str = Field(
        ...,
        description="Explanation of why one source is preferred over the other",
        examples=[
            "Source A is more recent (Jan 2026 vs Oct 2025) and directly from the company.",
            "Source A is a primary regulatory document with higher authority weight.",
        ],
    )
    severity: ConflictSeverity = Field(
        default="Minor",
        description="Severity: Minor (<5% diff), Moderate (5–20%), Major (>20%)",
    )
    date_a: Optional[str] = Field(
        default=None,
        description="Publication date of source A",
    )
    date_b: Optional[str] = Field(
        default=None,
        description="Publication date of source B",
    )

    @computed_field
    @property
    def conflict_summary(self) -> str:
        """One-line summary of the conflict for UI display."""
        return (
            f"{self.field}: {self.source_a} says {self.value_a} "
            f"vs {self.source_b} says {self.value_b}"
        )


# ─────────────────────────────────────────────
# 3. DataGap
# ─────────────────────────────────────────────

class DataGap(BaseModel):
    """
    Represents a piece of information the system could not find
    in the retrieved corpus. Signals knowledge limitations.
    """

    description: str = Field(
        ...,
        description="What information is missing",
        examples=["Q4 FY26 NPA data not available in corpus"],
    )
    impact: Literal["High", "Medium", "Low"] = Field(
        default="Low",
        description="How much this gap impacts answer quality",
    )
    suggested_source: Optional[str] = Field(
        default=None,
        description="Where this data could be found",
        examples=["BSE filing", "RBI database", "Company investor relations"],
    )


# ─────────────────────────────────────────────
# 4. AgentTrace
# ─────────────────────────────────────────────

class AgentTrace(BaseModel):
    """
    Execution trace for a single agent — attached to QueryResponse
    when debug_mode is enabled. Useful for pipeline debugging.
    """

    agent_name: str
    duration_ms: float
    success: bool
    input_summary: Optional[str] = None
    output_summary: Optional[str] = None
    error: Optional[str] = None

    @computed_field
    @property
    def status_label(self) -> str:
        if not self.success:
            return "FAILED"
        if self.duration_ms > 1000:
            return "SLOW"
        return "OK"


