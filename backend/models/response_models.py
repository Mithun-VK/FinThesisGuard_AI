# 7. ServiceHealth
# ─────────────────────────────────────────────

class ServiceHealth(BaseModel):
    """Health status of a single dependent service."""

    name: str = Field(..., description="Service name: groq, pinecone, redis")
    status: ServiceStatus = Field(..., description="Service status")
    latency_ms: Optional[float] = Field(
        default=None,
        description="Response time of health check ping",
    )
    version: Optional[str] = Field(
        default=None,
        description="Service version if available",
    )
    error: Optional[str] = Field(
        default=None,
        description="Error message if status is not 'ok'",
    )
    checked_at: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat(),
        description="UTC timestamp of health check",
    )

    @computed_field
    @property
    def is_healthy(self) -> bool:
        return self.status == "ok"


