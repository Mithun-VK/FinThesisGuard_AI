# 7. ThesisResponse
# ─────────────────────────────────────────────

class ThesisResponse(BaseModel):
    """
    Complete investment thesis validation response.
    Returned by POST /api/validate-thesis.

    Contains:
    - Extracted assumptions with confidence scores
    - Visual dependency chain
    - Risk scores per dimension (1–10)
    - Break conditions with trigger metrics
    - Historical analogs
    - Overall thesis strength and confidence
    - Supporting citations from corpus
    """

    # ── Core thesis ───────────────────────────
    thesis_text: str = Field(
        ...,
        description="The original thesis text that was validated",
    )

    # ── Verdict ───────────────────────────────
    thesis_strength: ThesisStrength = Field(
        ...,
        description=(
            "Overall thesis strength: Strong (avg risk <4), "
            "Medium (4–7), Weak (>7)"
        ),
    )
    structural_robustness: StructuralRobustness = Field(
        ...,
        description="How well the thesis logic holds together structurally",
    )
    confidence: ConfidenceLevel = Field(
        ...,
        description="Agent's confidence in the validation based on corpus coverage",
    )

    # ── Agent 6 outputs ───────────────────────
    assumptions: list[Assumption] = Field(
        default_factory=list,
        description="List of extracted assumptions from the thesis",
    )
    dependency_chain: list[str] = Field(
        default_factory=list,
        description="Ordered causal chain: ['AI Demand', 'Capex Growth', 'Revenue', 'Margins', 'Valuation']",
    )
    dependency_nodes: list[DependencyNode] = Field(
        default_factory=list,
        description="Structured dependency nodes for visual graph rendering",
    )

    # ── Agent 5 outputs ───────────────────────
    quantitative_flags: list[QuantitativeFlag] = Field(
        default_factory=list,
        description="Numerical inconsistencies detected in the thesis claims",
    )

    # ── Agent 7 outputs ───────────────────────
    risks: list[RiskScore] = Field(
        default_factory=list,
        description="Risk scores per dimension (demand, margin, valuation, etc.)",
    )
    break_conditions: list[BreakCondition] = Field(
        default_factory=list,
        description="Specific conditions that would invalidate the thesis",
    )
    historical_analogs: list[HistoricalAnalog] = Field(
        default_factory=list,
        description="Historical events structurally similar to this thesis",
    )
    synthesis: str = Field(
        default="",
        description="Agent's final synthesis paragraph summarizing the validation",
    )

    # ── Sources ───────────────────────────────
    citations: list[Citation] = Field(
        default_factory=list,
        description="Source documents used in the validation",
    )

    # ── Performance metadata ──────────────────
    latency_ms: int = Field(
        default=0,
        ge=0,
        description="Total pipeline execution time in milliseconds",
    )
    agents_used: list[str] = Field(
        default_factory=list,
        description="Agent names that processed this thesis",
    )
    cache_hit: bool = Field(default=False)
    request_id: Optional[str] = Field(default=None)
    timestamp: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat(),
    )

    # ─────────────────────────────────────────
    # COMPUTED FIELDS
    # ─────────────────────────────────────────

    @computed_field
    @property
    def avg_risk_score(self) -> float:
        """Average risk score across all dimensions."""
        if not self.risks:
            return 0.0
        return round(sum(r.score for r in self.risks) / len(self.risks), 2)

    @computed_field
    @property
    def highest_risk(self) -> Optional[RiskScore]:
        """The single highest-scoring (most dangerous) risk dimension."""
        if not self.risks:
            return None
        return max(self.risks, key=lambda r: r.score)

    @computed_field
    @property
    def critical_assumptions(self) -> list[Assumption]:
        """Assumptions marked as critical (thesis breaks without them)."""
        return [a for a in self.assumptions if a.is_critical]

    @computed_field
    @property
    def unsupported_assumptions(self) -> list[Assumption]:
        """Assumptions with no historical support in corpus."""
        return [a for a in self.assumptions if not a.historical_support]

    @computed_field
    @property
    def triggered_break_conditions(self) -> list[BreakCondition]:
        """Break conditions that have already triggered."""
        return [b for b in self.break_conditions if b.triggered]

    @computed_field
    @property
    def high_probability_breaks(self) -> list[BreakCondition]:
        """Break conditions with High probability of triggering."""
        return [b for b in self.break_conditions if b.probability == "High"]

    @computed_field
    @property
    def assumption_support_rate(self) -> float:
        """Percentage of assumptions with historical corpus support."""
        if not self.assumptions:
            return 0.0
        supported = sum(1 for a in self.assumptions if a.historical_support)
        return round(supported / len(self.assumptions) * 100, 1)

    @computed_field
    @property
    def citation_count(self) -> int:
        return len(self.citations)

    @computed_field
    @property
    def within_latency_target(self) -> bool:
        return self.latency_ms <= 3000

    @computed_field
    @property
    def verdict_summary(self) -> str:
        """
        One-line verdict string for UI header display.
        e.g. "Medium Thesis | High Confidence | Avg Risk: 5.4/10"
        """
        return (
            f"{self.thesis_strength} Thesis | "
            f"{self.confidence} Confidence | "
            f"Avg Risk: {self.avg_risk_score}/10"
        )

    # ─────────────────────────────────────────
    # VALIDATORS
    # ─────────────────────────────────────────

    @model_validator(mode="after")
    def validate_thesis_strength_consistency(self) -> "ThesisResponse":
        """
        Validates that thesis_strength is consistent with avg_risk_score.
        Auto-corrects if Agent 7 made an error.
        """
        avg = self.avg_risk_score
        if avg == 0.0:
            return self  # No risks scored yet — skip validation

        expected_strength: ThesisStrength
        strong_threshold = THESIS_STRENGTH_THRESHOLDS.get("strong", 4.0)
        medium_threshold = THESIS_STRENGTH_THRESHOLDS.get("medium", 7.0)

        if avg < strong_threshold:
            expected_strength = "Strong"
        elif avg < medium_threshold:
            expected_strength = "Medium"
        else:
            expected_strength = "Weak"

        if self.thesis_strength != expected_strength:
            # Auto-correct silently (agents can be slightly off)
            object.__setattr__(self, "thesis_strength", expected_strength)

        return self

    @model_validator(mode="after")
    def validate_assumption_ids_unique(self) -> "ThesisResponse":
        """Ensures all assumption IDs are unique."""
        ids = [a.id for a in self.assumptions]
        if len(ids) != len(set(ids)):
            raise ValueError("Assumption IDs must be unique.")
        return self

    # ─────────────────────────────────────────
    # CONVENIENCE CONSTRUCTORS
    # ─────────────────────────────────────────

    @classmethod
    def error_response(
        cls,
        thesis_text: str,
        error_message: str,
        request_id: Optional[str] = None,
        latency_ms: int = 0,
    ) -> "ThesisResponse":
        """Creates a standardized error response for failed thesis validation."""
        return cls(
            thesis_text=thesis_text,
            thesis_strength="Weak",
            structural_robustness="Low",
            confidence="Low",
            synthesis=f"Thesis validation failed: {error_message}",
            latency_ms=latency_ms,
            request_id=request_id,
        )

    @classmethod
    def insufficient_data_response(
        cls,
        thesis_text: str,
        request_id: Optional[str] = None,
        latency_ms: int = 0,
    ) -> "ThesisResponse":
        """Creates a response when corpus has insufficient data for validation."""
        return cls(
            thesis_text=thesis_text,
            thesis_strength="Medium",
            structural_robustness="Low",
            confidence="Low",
            synthesis=(
                "Insufficient corpus data to fully validate this thesis. "
                "Please ingest relevant annual reports, earnings transcripts, "
                "and sector research before validation."
            ),
            latency_ms=latency_ms,
            request_id=request_id,
        )

    model_config = {"populate_by_name": True}


