# 2. RiskScore
# ─────────────────────────────────────────────

class RiskScore(BaseModel):
    """
    Risk score for a single dimension of the investment thesis.
    Generated by Agent 7 (Stress Synthesizer).

    Score 1–10: 1 = minimal risk, 10 = severe/thesis-breaking risk.
    """

    dimension: str = Field(
        ...,
        description="Risk dimension name",
        examples=THESIS_RISK_DIMENSIONS,
    )
    score: int = Field(
        ...,
        ge=1,
        le=10,
        description="Risk score from 1 (low) to 10 (critical)",
    )
    rationale: str = Field(
        ...,
        min_length=20,
        max_length=1000,
        description="Agent's explanation of the risk score",
        examples=[
            "Valuation risk is high (9/10) because the stock trades at 35x FY27E "
            "earnings, well above its 5-year average of 22x, leaving little margin "
            "of safety if earnings miss by even 5%."
        ],
    )
    supporting_data: list[str] = Field(
        default_factory=list,
        max_length=5,
        description="Data points from corpus used to justify this risk score",
    )
    related_assumption_ids: list[int] = Field(
        default_factory=list,
        description="IDs of assumptions this risk score relates to",
    )

    @field_validator("dimension")
    @classmethod
    def validate_dimension(cls, v: str) -> str:
        return v.lower().strip().replace(" ", "_")

    @computed_field
    @property
    def severity_label(self) -> Literal["Critical", "High", "Medium", "Low"]:
        """Maps score to severity label."""
        if self.score >= 9:
            return "Critical"
        if self.score >= 7:
            return "High"
        if self.score >= 4:
            return "Medium"
        return "Low"

    @computed_field
    @property
    def severity_color(self) -> Literal["red", "orange", "yellow", "green"]:
        """UI color for risk ring component."""
        if self.score >= 8:
            return "red"
        if self.score >= 6:
            return "orange"
        if self.score >= 4:
            return "yellow"
        return "green"

    @computed_field
    @property
    def dimension_label(self) -> str:
        """Human-readable dimension name for UI display."""
        labels = {
            "demand_risk":      "Demand Risk",
            "margin_risk":      "Margin Risk",
            "valuation_risk":   "Valuation Risk",
            "regulatory_risk":  "Regulatory Risk",
            "macro_risk":       "Macro Risk",
            "competitive_risk": "Competitive Risk",
        }
        return labels.get(self.dimension, self.dimension.replace("_", " ").title())


