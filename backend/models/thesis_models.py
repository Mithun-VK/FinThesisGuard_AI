# ─────────────────────────────────────────────

class BreakCondition(BaseModel):
    """
    A specific, measurable event that would invalidate the thesis.
    Generated by Agent 7 (Stress Synthesizer).

    Each break condition is actionable — the investor can monitor
    the trigger metric and exit/adjust position if threshold is hit.
    """

    condition: str = Field(
        ...,
        min_length=10,
        max_length=500,
        description="Description of the break condition",
        examples=[
            "NIM compresses below 3.8% for two consecutive quarters",
            "Hyperscaler AI capex growth decelerates below 10% YoY",
            "SEBI introduces position limits for F&O market makers",
        ],
    )
    trigger_metric: str = Field(
        ...,
        max_length=200,
        description="Specific measurable metric to monitor",
        examples=["NIM", "Hyperscaler capex growth YoY", "SEBI F&O regulation"],
    )
    threshold: str = Field(
        ...,
        max_length=200,
        description="The level at which the thesis breaks",
        examples=["Below 3.8%", "Below 10% YoY", "Any new restrictive circular"],
    )
    probability: BreakProbability = Field(
        ...,
        description="Agent's assessment of likelihood this condition triggers",
    )
    data_source: str = Field(
        default="corpus",
        max_length=200,
        description="Source used to assess this break condition's probability",
        examples=["RBI Monetary Policy FY26", "Gartner AI Spending Forecast 2025"],
    )
    monitoring_frequency: Optional[str] = Field(
        default=None,
        description="How often to check this metric",
        examples=["Quarterly (earnings)", "Monthly (RBI data)", "Real-time (news)"],
    )
    triggered: bool = Field(
        default=False,
        description="True if current data shows this condition has already triggered",
    )
    related_assumption_ids: list[int] = Field(
        default_factory=list,
        description="IDs of assumptions this break condition is linked to",
    )

    @field_validator("condition", "trigger_metric", "threshold", mode="before")
    @classmethod
    def normalize_strings(cls, v: str) -> str:
        return v.strip() if isinstance(v, str) else v

    @computed_field
    @property
    def urgency_label(self) -> str:
        """UI urgency label combining probability and triggered status."""
        if self.triggered:
            return "TRIGGERED"
        if self.probability == "High":
            return "MONITOR CLOSELY"
        if self.probability == "Medium":
            return "WATCH"
        return "LOW RISK"

    @computed_field
    @property
    def urgency_color(self) -> Literal["red", "orange", "yellow", "green"]:
        if self.triggered:
            return "red"
        if self.probability == "High":
            return "orange"
        if self.probability == "Medium":
            return "yellow"
        return "green"


