class HistoricalAnalog(BaseModel):
    """
    A historical event/period that is structurally similar to the thesis.
    Helps investors understand precedent and potential outcomes.
    """

    title: str = Field(
        ...,
        max_length=200,
        description="Name of the historical analog",
        examples=[
            "Indian Banking NIM Expansion Cycle FY2003-2007",
            "Semiconductor Capex Cycle Peak 2022",
            "Telecom ARPU Compression 2017 (Jio Disruption)",
        ],
    )
    period: str = Field(
        ...,
        max_length=100,
        description="Time period of the analog",
        examples=["FY2003–FY2007", "2021–2022", "2017–2019"],
    )
    similarity_score: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="How similar this analog is to the current thesis (0.0–1.0)",
    )
    outcome: str = Field(
        ...,
        max_length=500,
        description="What happened in the historical analog",
    )
    lesson: str = Field(
        ...,
        max_length=500,
        description="Key lesson for the current thesis from this analog",
    )
    source: Optional[str] = Field(default=None, max_length=200)

    @computed_field
    @property
    def similarity_label(self) -> str:
        if self.similarity_score >= 0.75:
            return "Highly Similar"
        if self.similarity_score >= 0.5:
            return "Moderately Similar"
        return "Loosely Similar"


