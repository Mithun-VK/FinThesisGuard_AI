# ThesisCompareResponse
# ─────────────────────────────────────────────

class ThesisCompareResponse(BaseModel):
    """
    Side-by-side comparison of two thesis validations.
    Returned by POST /api/thesis/compare.
    """

    thesis_a: ThesisResponse
    thesis_b: ThesisResponse
    winner: Literal["thesis_a", "thesis_b", "tie"] = Field(
        ...,
        description="Which thesis is stronger overall",
    )
    comparison_summary: str = Field(
        default="",
        description="Agent's summary of the comparative analysis",
    )
    latency_ms: int = Field(default=0)
    timestamp: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat(),
    )

    @computed_field
    @property
    def risk_delta(self) -> float:
        """Difference in avg risk scores (A minus B). Positive = A is riskier."""
        return round(thesis_a_score - thesis_b_score, 2) if (
            thesis_a_score := self.thesis_a.avg_risk_score
        ) and (
            thesis_b_score := self.thesis_b.avg_risk_score
        ) else 0.0


