# 4. DependencyNode
# ─────────────────────────────────────────────

class DependencyNode(BaseModel):
    """
    A single node in the thesis dependency chain.
    Used to build the visual A → B → C → D flow diagram.
    """

    id: int
    label: str = Field(..., max_length=60, description="Short label for the node")
    description: str = Field(default="", max_length=200)
    depends_on: list[int] = Field(default_factory=list)
    risk_score: Optional[int] = Field(
        default=None,
        ge=1,
        le=10,
        description="Risk score associated with this node (if applicable)",
    )
    is_terminal: bool = Field(
        default=False,
        description="True if this is the final outcome node",
    )

    @computed_field
    @property
    def node_color(self) -> Literal["red", "orange", "green", "gray"]:
        if self.risk_score is None:
            return "gray"
        if self.risk_score >= 7:
            return "red"
        if self.risk_score >= 4:
            return "orange"
        return "green"


