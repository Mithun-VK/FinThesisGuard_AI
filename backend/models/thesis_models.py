# 1. Assumption
# ─────────────────────────────────────────────

class Assumption(BaseModel):
    """
    A single extracted assumption from the investment thesis.
    Generated by Agent 6 (Thesis Analyzer).

    Each assumption represents a causal claim the thesis depends on.
    Example: "AI demand from hyperscalers will grow 30%+ YoY"
    """

    id: int = Field(
        ...,
        ge=1,
        le=THESIS_MAX_ASSUMPTIONS,
        description="Assumption sequence number (1 to max)",
    )
    text: str = Field(
        ...,
        min_length=10,
        max_length=500,
        description="The assumption statement extracted from the thesis",
        examples=[
            "Hyperscaler AI capex will grow 25%+ in FY27",
            "HDFC Bank CASA ratio will stay above 40%",
            "RBI will not tighten LCR norms in next 12 months",
        ],
    )
    category: AssumptionCategory = Field(
        default="unknown",
        description="Type of assumption: demand, margin, valuation, regulatory, macro, etc.",
    )
    confidence: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="Agent's confidence that this assumption is valid (0.0–1.0)",
    )
    historical_support: bool = Field(
        ...,
        description="True if historical data in corpus supports this assumption",
    )
    supporting_evidence: list[str] = Field(
        default_factory=list,
        max_length=5,
        description="List of evidence snippets from corpus supporting/refuting this assumption",
        examples=[["HDFC Bank CASA was 41.9% in Q3 FY26 (Annual Report 2026)"]],
    )
    dependency_on: list[int] = Field(
        default_factory=list,
        description="IDs of other assumptions this one depends on",
    )
    is_critical: bool = Field(
        default=False,
        description="True if thesis completely breaks if this assumption fails",
    )

    @field_validator("text", mode="before")
    @classmethod
    def normalize_text(cls, v: str) -> str:
        return v.strip() if isinstance(v, str) else v

    @field_validator("confidence")
    @classmethod
    def round_confidence(cls, v: float) -> float:
        return round(v, 3)

    @computed_field
    @property
    def confidence_label(self) -> ConfidenceLevel:
        """Maps float confidence to High/Medium/Low label."""
        if self.confidence >= 0.7:
            return "High"
        if self.confidence >= 0.4:
            return "Medium"
        return "Low"

    @computed_field
    @property
    def risk_flag(self) -> str:
        """UI flag for assumption card color coding."""
        if not self.historical_support and self.is_critical:
            return "critical"
        if not self.historical_support:
            return "warning"
        if self.confidence < 0.4:
            return "caution"
        return "supported"


